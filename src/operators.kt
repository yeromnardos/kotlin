fun main(args: Array<String>) {
//    mathematical operator
    var num=5
    var num1=3
    println("The sum of $num and $num1 is ${num+num1}")
    println("The difference of $num and $num1 is ${num-num1}")
    println("The quotient of $num and $num1 is ${num/num1}")
    println("The reminder of $num and $num1 is ${num%num1}")
    println("The multiplication of $num and $num1 is ${num*num1}")
}